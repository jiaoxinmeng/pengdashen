package com.zaasbase.bean;

public class Expert {
	
	private int id;
	private String expertName;
	private String expertContent;
	private String expertType;
	private int expertOrder;
	private String expertSummary;
	private String expertImg;
	private int expertTeam;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getExpertName() {
		return expertName;
	}
	public void setExpertName(String expertName) {
		this.expertName = expertName;
	}
	public String getExpertContent() {
		return expertContent;
	}
	public void setExpertContent(String expertContent) {
		this.expertContent = expertContent;
	}
	public String getExpertType() {
		return expertType;
	}
	public void setExpertType(String expertType) {
		this.expertType = expertType;
	}
	public int getExpertOrder() {
		return expertOrder;
	}
	public void setExpertOrder(int expertOrder) {
		this.expertOrder = expertOrder;
	}
	public String getExpertSummary() {
		return expertSummary;
	}
	public void setExpertSummary(String expertSummary) {
		this.expertSummary = expertSummary;
	}
	public String getExpertImg() {
		return expertImg;
	}
	public void setExpertImg(String expertImg) {
		this.expertImg = expertImg;
	}
	public int getExpertTeam() {
		return expertTeam;
	}
	public void setExpertTeam(int expertTeam) {
		this.expertTeam = expertTeam;
	}
	
}
